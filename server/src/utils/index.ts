export const addTwoNumbers = (number1: number, number2: number) => {
  const sum = number1 + number1;
  return sum;
};

export const subtractTwoNumbers = (number1: number, number2: number) => {
  const subtract = number1 - number1;
  return subtract;
};

export const multipleTwoNumbers = (number1: number, number2: number) => {
  const multiply = number1 * number1;
  return multiply;
};

export const divideTwoNumber = (number1: number, number2: number) => {
  const divide = number1 / number1;
  return divide;
};
